// ─── Core helpers ─────────────────────────────────────────────────────────────
const translations: Record<Lang, typeof es> = { es, ca, en };

/** Get a translated string by dot-notation key, e.g. t("auth.login.button", "ca") */
export function t(key: string, lang: Lang): string {
  const parts = key.split(".");
  let node: any = translations[lang];
  for (const p of parts) {
    if (node == null || typeof node !== "object") return key;
    node = node[p];
  }
  return typeof node === "string" ? node : key;
}

/** Get the onboarding slides for a given language */
export function getSlides(lang: Lang) {
  return translations[lang].onboarding.slides;
}
